
public class ForLoopSecond {

	public static void main(String[] args) {
		// to print * in pyramid format
		
		for(int i=1; i<6; i++)
		//this for loop is used to create rows
		{
			for(int z=0; z<i; z++)
			{
				System.out.print("*");
				//this will print * in the first row
			}
		        System.out.println();
		        //this will move cursor to next line
	}

}
}